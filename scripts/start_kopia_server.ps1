# start_kopia_server.ps1 — launcher invoked by \Backup\KopiaServer scheduled task
# at system startup. Reads the server API password from the DPAPI vault, exports
# it as KOPIA_SERVER_PASSWORD, and execs kopia.exe in long-running server mode.
#
# Owns the kopia repository on disk. Both the daily backup wrapper and KopiaUI
# (configured in client mode) talk to this server via its REST API on
# 127.0.0.1:51515 with the pinned TLS fingerprint at D:\KopiaServer\fingerprint.sha256.
#
# Stays in foreground so the scheduled task's Restart-on-failure semantics work.
# DPAPI LocalMachine decrypt requires this process to run elevated (RunLevel=Highest
# in the task definition).

[CmdletBinding()]
param(
    [string]$KopiaBin   = 'C:\Users\david\go\bin\kopia.exe',
    # Server-side config is filesystem-mode (direct repo open). The CLIENT-side
    # config at %APPDATA%\kopia\repository.config is API-mode pointing at this
    # very server — using it here would create a self-referential connect loop.
    [string]$ConfigFile = 'D:\KopiaServer\repository.config',
    [string]$CertFile   = 'D:\KopiaServer\server.crt',
    [string]$KeyFile    = 'D:\KopiaServer\server.key',
    [string]$Address    = '127.0.0.1:51515',
    [string]$VaultHelper     = 'C:\dev\kopia\scripts\get_kopia_server_password.ps1',
    [string]$RepoVaultHelper = 'C:\dev\kopia\scripts\get_kopia_password.ps1'
)

$ErrorActionPreference = 'Stop'

# Resolve both passwords from DPAPI vaults:
#   KOPIA_SERVER_PASSWORD: HTTP basic auth password for kopia API clients
#   KOPIA_PASSWORD:        repository encryption password (decrypts D:\KopiaRepo)
# Setting both via env avoids depending on Windows Credential Manager, which
# may be inaccessible from S4U / elevated split-token contexts.
$apiPw = & $VaultHelper
if (-not $apiPw) {
    Write-Error "Empty server-API password from $VaultHelper"
    exit 1
}
$env:KOPIA_SERVER_PASSWORD = $apiPw

$repoPw = & $RepoVaultHelper
if (-not $repoPw) {
    Write-Error "Empty repository password from $RepoVaultHelper"
    exit 1
}
$env:KOPIA_PASSWORD = $repoPw

# Hand off to kopia.exe. Stays in foreground; the scheduled task's
# Restart-on-failure setting handles unexpected exits.
#
# --heartbeat-* (epic kopia-bcp): predictable proof-of-life signal every 60s.
# Downstream stall guards (wrapper, watchdog, backup-monitor.exe) consume the
# heartbeat-line freshness in C:\dev\kopia\logs\heartbeat.log.
& $KopiaBin server start `
    --address=$Address `
    --server-username=kopia `
    --tls-cert-file=$CertFile `
    --tls-key-file=$KeyFile `
    --config-file=$ConfigFile `
    --error-notifications=always `
    --heartbeat-interval=60s `
    --heartbeat-file=C:\dev\kopia\logs\heartbeat.log

exit $LASTEXITCODE

